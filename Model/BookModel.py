class Book:
    def __init__(self, book_name, book_code, author_id, book_type, price, isbn, publish_date, paperback):
        self.BookName = book_name
        self.BookCode = book_code
        self.AuthorID = author_id
        self.BookType = book_type
        self.Price = price
        self.ISBN = isbn
        self.PublishDate = publish_date
        self.Paperback = paperback
